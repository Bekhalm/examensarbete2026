//
// SR's web pages (sverigesradio.se) sit behind a WAF that returns 403 to our
// monitor, so scraping them is hopeless. SR does, however, publish clean,
// stable RSS/Atom for every channel's news desk via its public API at
// api.sr.se. This is exactly how the "Ekot" source works (program 83).
//
// This module maps the page URLs reporters naturally paste — e.g.
// https://www.sverigesradio.se/nyheter/p4-stockholm — to the matching
// api.sr.se program feed, and exposes the full P4 catalog so the UI can offer
// one-click on/off per channel.
//
