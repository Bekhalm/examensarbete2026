//   /api/v2/programs/index?channelid=...  -> each channel's "Nyheter P4 X" program
//   /api/rss/program/{id}                 -> verified to carry <link rel="alternate">
//                                            pointing back at the page slug below.
// (P4 Plus and P4 Södertälje have no local news desk and are intentionally absent.)

